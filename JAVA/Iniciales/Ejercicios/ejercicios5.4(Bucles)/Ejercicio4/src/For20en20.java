public class For20en20 {
    public static void main(String[] args) {
        System.out.println("*** Ejercicio 4 contar de 20 en 20 for ***");
        For20en20.contar20();
    }
    public static void contar20(){
        for (int i = 360; i>= 160; i-=20){
            System.out.println(i);
        }

    }
}
